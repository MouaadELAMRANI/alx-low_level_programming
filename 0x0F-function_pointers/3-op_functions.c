/**
 * op_add - add
 * @a: int
 * @b: int
 * Return: addition
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - sub
 * @a: int
 * @b: int
 * Return: diff
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multi
 * @a: int
 * @b: int
 * Return: multiplication
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - div
 * @a: int
 * @b: int
 * Return: division
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - mod
 * @a: int
 * @b: int
 * Return: mod
 */
int op_mod(int a, int b)
{
	return (a % b);
}
